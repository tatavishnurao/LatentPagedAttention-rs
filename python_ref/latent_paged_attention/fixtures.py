def paged_kv_write_fixture() -> dict:
    """Return deterministic full-cache K/V write cases."""
    num_physical_blocks = 4
    block_size = 2
    kv_heads = 2
    head_dim = 4
    width = kv_heads * head_dim
    block_table = np.asarray([2, 0, 1], dtype=np.int32)
    padded_block_table = np.asarray([2, 0, 1, 3], dtype=np.int32)
    initial_k = np.arange(num_physical_blocks * block_size * width, dtype=np.float32).reshape(
        num_physical_blocks, block_size, width
    )
    initial_v = (
        np.arange(num_physical_blocks * block_size * width, dtype=np.float32) + 1000
    ).reshape(num_physical_blocks, block_size, width)
    writes = (
        (
            "position_3_offset_1",
            3,
            np.arange(100, 108, dtype=np.float32),
            np.arange(200, 208, dtype=np.float32),
        ),
        (
            "position_4_offset_0",
            4,
            np.arange(300, 308, dtype=np.float32),
            np.arange(400, 408, dtype=np.float32),
        ),
    )
    cases = []
    for name, token_position, new_k, new_v in writes:
        logical_block, physical_block, block_offset = resolve_paged_token_location(
            block_table, token_position, block_size
        )
        expected_k, expected_v = paged_kv_write_ref(
            initial_k, initial_v, block_table, token_position, new_k, new_v
        )
        cases.append(
            {
                "name": name,
                "token_position": token_position,
                "logical_block": logical_block,
                "physical_block": physical_block,
                "block_offset": block_offset,
                "initial_k_cache": initial_k.tolist(),
                "initial_v_cache": initial_v.tolist(),
                "new_k": new_k.tolist(),
                "new_v": new_v.tolist(),
                "expected_k_cache": expected_k.tolist(),
                "expected_v_cache": expected_v.tolist(),
            }
        )
    return {
        "dtype": "f32",
        "num_physical_blocks": num_physical_blocks,
        "block_size": block_size,
        "kv_heads": kv_heads,
        "head_dim": head_dim,
        "width": width,
        "block_table": block_table.tolist(),
        "gpu_padded_block_table": padded_block_table.tolist(),
        "cases": cases,
    }


def _to_head_major(cache: np.ndarray) -> np.ndarray:
    """Convert [seq_len, kv_heads, head_dim] to [kv_heads, seq_len, head_dim]."""
    return np.transpose(cache, (1, 0, 2)).copy()


def _physical_to_gpu_head_major(cache: np.ndarray) -> np.ndarray:
    """Convert physical token-major blocks to the GPU head-major block layout."""
    return np.transpose(cache, (0, 2, 1, 3)).copy()


def _balanced_gqa_case() -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    q_heads = 4
    kv_heads = 2
    seq_len = 8
    head_dim = 8
    q = np.zeros((1, q_heads, head_dim), dtype=np.float32)
    k = np.zeros((1, seq_len, kv_heads, head_dim), dtype=np.float32)
    v = np.zeros((1, seq_len, kv_heads, head_dim), dtype=np.float32)
    for q_head in range(q_heads):
        for dim in range(head_dim):
            q[0, q_head, dim] = (
                (q_head + 1) * 0.17 + (dim + 1) * 0.031 + ((q_head + dim) % 3) * 0.007
            )
    for token in range(seq_len):
        for kv_head in range(kv_heads):
            for dim in range(head_dim):
                k[0, token, kv_head, dim] = (
                    (kv_head + 1) * 0.23
                    + (token + 1) * 0.019
                    - (dim + 1) * 0.011
                    + ((token + dim + kv_head) % 4) * 0.005
                )
                v[0, token, kv_head, dim] = (
                    (kv_head + 1) * 0.41
                    - (token + 1) * 0.013
                    + (dim + 1) * 0.027
                    + ((2 * token + dim + kv_head) % 5) * 0.003
                )
    return q, k, v


def _stable_softmax_gqa_case() -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    q_heads = 4
    kv_heads = 2
    seq_len = 8
    head_dim = 8
    q = np.zeros((1, q_heads, head_dim), dtype=np.float32)
    k = np.zeros((1, seq_len, kv_heads, head_dim), dtype=np.float32)
    v = np.zeros((1, seq_len, kv_heads, head_dim), dtype=np.float32)
    for q_head in range(q_heads):
        sign = -1.0 if q_head % 2 else 1.0
        for dim in range(head_dim):
            q[0, q_head, dim] = sign * (6.0 + q_head * 1.25 + dim * 0.5)
    for token in range(seq_len):
        for kv_head in range(kv_heads):
            token_scale = (token - 3.5) * (1.6 + kv_head * 0.35)
            for dim in range(head_dim):
                k[0, token, kv_head, dim] = token_scale + dim * 0.42 - kv_head * 0.9
                v[0, token, kv_head, dim] = (
                    (token + 1) * 0.21 + (kv_head + 1) * 0.33 - dim * 0.017
                )
    return q, k, v


